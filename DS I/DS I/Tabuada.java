package tabuada;
public class Tabuada {
    public static void main(String[] args) {
        
        int x;
        
        x = 2;
        
        for (int i = 0; i <= 10; i++){
            int res = x * i;
            System.out.println(x + "x" + i + "=" + res);
        
    }
    }
}
